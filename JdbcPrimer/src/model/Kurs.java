package model;

public class Kurs {
	
	private int idKursa;
	private String imeKursa;
	private double cena;
	
	public int getIdKursa() {
		return idKursa;
	}
	public void setIdKursa(int idKursa) {
		this.idKursa = idKursa;
	}
	public String getImeKursa() {
		return imeKursa;
	}
	public void setImeKursa(String imeKursa) {
		this.imeKursa = imeKursa;
	}
	public double getCena() {
		return cena;
	}
	public void setCena(double cena) {
		this.cena = cena;
	}
	
	
	
	

}
